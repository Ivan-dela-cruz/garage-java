    public void listar(){
        String Titulo[]={"Id","Cedula","Nombre","Apellido","Telefono","Direcci√≤n","Placa","Modelo","Color"};
        String registro[]=new String[9];
        modelo= new DefaultTableModel(null,Titulo);
        Conexion j=new Conexion(); 
        j.conectar();
        sql=j.sql;
        try {
            s = sql.createStatement();
            ResultSet rs = s.executeQuery("select * from cliente_mes");
            while (rs.next()){
                registro[0]=rs.getString("id_cli");
                registro[1]=rs.getString("cedula");
                registro[2]=rs.getString("nombre");
                registro[3]=rs.getString("apellido");
                registro[4]=rs.getString("telefono");
                registro[5]=rs.getString("direccion");
                registro[6]=rs.getString("placa");
                registro[7]=rs.getString("modelo");
                registro[8]=rs.getString("color");
                modelo.addRow(registro);
            }
            jTable2.setModel(modelo);
        } catch (SQLException ex) {
            
        }
}

    
